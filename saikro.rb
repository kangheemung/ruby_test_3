## **D117:すごろくのサイコロ**

#あなたはすごろくをしています。このすごろくでは、目が1から6まである通常のサイコロをいくつか使用し、出た目の数の合計だけ駒を前に進めます。

#使用するサイコロの数と出た目が与えられるので、何マス前に進めるのか計算してください。

#例えば、入力例 1 では

#6 個のサイコロの目がそれぞれ 4 , 5 , 2 , 3 , 6 , 1 なので、合計の 21 を出力します。

#条件すべてのテストケースにおいて、以下の条件をみたします。
#• ・0 ≦ N ≦ 100
#• ・1 ≦ a_i ≦ 6

n = gets.to_i

a_i = gets.chomp.split(" ").map(&:to_i)

if n >= 0 && n <= 100
    if a_i.all? { |i| i >= 1 && i<= 6 }
      total = a_i.sum
      puts total
    else
      puts "no"
    
    end
else
  puts "no"
end